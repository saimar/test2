package com.mx.bancoazteca.pld.monitoreo.bean;

public class ColoresVO {
	String consulta;
	String id;
	String color;
	String status;
	String clasificacion;
	String idOrden;
	String desColor;
	String desOrden;
	public String getConsulta() {
		return consulta;
	}
	public void setConsulta(String consulta) {
		this.consulta = consulta;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getClasificacion() {
		return clasificacion;
	}
	public void setClasificacion(String clasificacion) {
		this.clasificacion = clasificacion;
	}
	public String getIdOrden() {
		return idOrden;
	}
	public void setIdOrden(String idOrden) {
		this.idOrden = idOrden;
	}
	public String getDesColor() {
		return desColor;
	}
	public void setDesColor(String desColor) {
		this.desColor = desColor;
	}
	public String getDesOrden() {
		return desOrden;
	}
	public void setDesOrden(String desOrden) {
		this.desOrden = desOrden;
	}
	
	
}